    public void loadTable(ArrayList<QuanLyPhong> list){
        defaultTableModel = (DefaultTableModel) tblBang.getModel();
        defaultTableModel.setRowCount(0);
        for(QuanLyPhong quanLyPhong : list){
            defaultTableModel.addRow(new Object[]{
                quanLyPhong.getSoPhong(),
                quanLyPhong.getKhuVuc(),
                quanLyPhong.loaiPhong()
            });
        }
    }
    public void addCbbox(){
        ArrayList<String> listKhuVuc = new ArrayList<>();
        listKhuVuc.add("Hà Nội");
        listKhuVuc.add("Thái Bình");
        listKhuVuc.add("Nam Định");
        defaultComboBoxModel = (DefaultComboBoxModel) cbKhuVuc.getModel();
        for(String khuVuc : listKhuVuc){
            defaultComboBoxModel.addElement(khuVuc);
            
        }
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        QuanLyPhong qlp = new QuanLyPhong();
        String soPhong = txtSoPhong.getText();
        String khuVuc = cbKhuVuc.getSelectedItem().toString();
        Boolean loaiPhong = false;
        if(rdoPhongDon.isSelected()){
            loaiPhong=true;
        }
        qlp.setSoPhong(soPhong);
        qlp.setKhuVuc(khuVuc);
        qlp.setLoaiPhong(loaiPhong);
        
        if(quanLyPhongSerivce.add(qlp)){
            JOptionPane.showMessageDialog(this, "Thêm thanh cong");
            loadTable(quanLyPhongSerivce.getList());
            
        }else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblBang.getSelectedRow();
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa");
            return;
        }
        if(quanLyPhongSerivce.xoa(index)){
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            loadTable(quanLyPhongSerivce.getList());
        }else{
            JOptionPane.showMessageDialog(this, "xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblBangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangMouseClicked
        int row = tblBang.getSelectedRow();
        txtSoPhong.setText(tblBang.getValueAt(row, 0).toString());
        cbKhuVuc.setSelectedItem(tblBang.getValueAt(row, 1).toString());
        if(tblBang.getValueAt(row, 2).toString().equalsIgnoreCase("Phòng đơn")){
            rdoPhongDon.setSelected(true);
        }else{
            rdoPhongKep.setSelected(true);
        }
    }//GEN-LAST:event_tblBangMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblBang.getSelectedRow();
        QuanLyPhong qlp = new QuanLyPhong();
        String soPhong = txtSoPhong.getText();
        String khuVuc = cbKhuVuc.getSelectedItem().toString();
        Boolean loaiPhong = false;
        if(rdoPhongDon.isSelected()){
            loaiPhong=true;
        }
        qlp.setSoPhong(soPhong);
        qlp.setKhuVuc(khuVuc);
        qlp.setLoaiPhong(loaiPhong);
        
        if(quanLyPhongSerivce.update(row, qlp)){
            JOptionPane.showMessageDialog(this, "Update thanh cong");
            loadTable(quanLyPhongSerivce.getList());
            
        }else{
            JOptionPane.showMessageDialog(this, "Update thất bại");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

